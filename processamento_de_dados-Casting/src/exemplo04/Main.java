package exemplo04;

public class Main {

	public static void main(String[] args) {
		
		int a, b;
		double resultado;
		
		a = 5;
		b = 2;
		
		// Casting = conversão explícita dos valores
		// Colocando a variável entre '()' em frente a expressão 
		resultado = (double) a / b;
		
		System.out.println(resultado);

	}

}
